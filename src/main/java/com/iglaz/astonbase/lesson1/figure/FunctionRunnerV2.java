package com.iglaz.astonbase.lesson1.figure;

public class FunctionRunnerV2 {
    public static void main(String[] args) {
        double pi = 3.14;

        double  radius1 = 5.2;
        double  circumference1 = 2 * pi * radius1;

        double  radius2 = 6.7;
        double  circumference2 = 2 * pi * radius2;

        double  radius3 = 10;
        double  circumference3 = 2 * pi * radius3;

        System.out.println(circumference1);
        System.out.println(circumference2);
        System.out.println(circumference3);
    }
}
